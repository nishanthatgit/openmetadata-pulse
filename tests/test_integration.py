@pytest.mark.asyncio
async def test_integration_filtered_payload():
    """
    Scenario 3: Filtered Payload Drop
    An event generated by the ingestion-bot is filtered out before Slack delivery.
    """
    payload = {
        "eventType": "entityCreated",
        "entityType": "table",
        "entityFullyQualifiedName": "sample.orders",
        "updatedBy": "ingestion-bot",
    }
    
    mock_client = MagicMock()
    mock_client.chat_postMessage = MagicMock()

    with patch("pulse.notifier._get_slack_client", return_value=mock_client), \
         patch("pulse.notifier.settings") as mock_settings:
        
        mock_settings.slack_bot_token = "xoxb-test"
        mock_settings.slack_default_channel = "#default-channel"
        mock_settings.om_server_url = "http://localhost:8585"

        async with httpx.AsyncClient(transport=httpx.ASGITransport(app=app), base_url="http://test") as client:
            resp = await client.post("/webhook", json=payload)
            
    # API still returns 200, but ignores the event internally
    assert resp.status_code == 200
    assert resp.json() == {"status": "ignored"}
    
    # Ensure Slack is NEVER called
    mock_client.chat_postMessage.assert_not_called()
